// Render renders a string of digits and colons into a 5-line block-letter banner.
func Render(s string) string {
	rows := make([]strings.Builder, 5)
	for _, r := range s {
		g, ok := glyphs[r]
		if !ok {
			g = glyphs[':']
		}
		for i := 0; i < 5; i++ {
			rows[i].WriteString(g[i])
			rows[i].WriteString("  ")
		}
	}
	out := make([]string, 5)
	for i := range rows {
		out[i] = rows[i].String()
	}
	return strings.Join(out, "\n")
}
